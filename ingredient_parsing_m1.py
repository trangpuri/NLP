UNICODE_FRACTION_MAP = {
    "\u00bd": "1/2", "\u2153": "1/3", "\u2154": "2/3",
    "\u00bc": "1/4", "\u00be": "3/4", "\u2155": "1/5",
    "\u2156": "2/5", "\u2157": "3/5", "\u2158": "4/5",
    "\u2159": "1/6", "\u215a": "5/6", "\u215b": "1/8",
    "\u215c": "3/8", "\u215d": "5/8", "\u215e": "7/8",
}

def normalize_unicode_fraction(text: str) -> str:
    for char, replacement in UNICODE_FRACTION_MAP.items():
        text = text.replace(char, replacement)
    return text

def convert_mixed_fraction(text: str) -> str:
    def repl(m):
        return str(round(float(m.group(1)) + float(m.group(2)) / float(m.group(3)), 4))
    return re.sub(r"(\d+)\s+(\d+)/(\d+)", repl, text)

def _repl_fraction(m):
    num, den = float(m.group(1)), float(m.group(2))
    return m.group(0) if den == 0 else str(round(num / den, 4))

def convert_fraction(text: str) -> str:
    parts = re.split(r"(\([^)]*\))", text)
    return "".join(
        part if part.startswith("(")
        else re.sub(r"\b(\d+)/(\d+)\b", _repl_fraction, part)
        for part in parts
    )

def normalize_numbers(items: list) -> list:
    result = []
    for text in items:
        text = normalize_unicode_fraction(text)
        text = convert_mixed_fraction(text)
        text = convert_fraction(text)
        result.append(text)
    return result

UNIT_MAP = {
    r"\btablespoons?\b|\btbsp\b|\btbs\b": "tablespoon",
    r"\bteaspoons?\b|\btsp\b":            "teaspoon",
    r"\bcups?\b":                         "cup",
    r"\boz\b|\bounces?\b":                "ounce",
    r"\blbs?\b|\bpounds?\b":              "pound",
    r"\bgrams?\b":                        "gram",
    r"\bkilograms?\b|\bkg\b":             "kilogram",
    r"\bmilliliters?\b|\bml\b":           "milliliter",
    r"\bliters?\b":                       "liter",
    r"\bcans?\b":                         "can",
    r"\bpackages?\b|\bpkg\b":             "package",
}
# Pattern: (about X unit) or (approximately X unit) — volume equivalents
VOLUME_EQUIV_RE = re.compile(
    r"\(\s*(?:about|approximately|roughly|around)?\s*"
    r"\d+(?:\.\d+)?\s*"
    r"(?:cup|tablespoon|teaspoon|ounce|pound|gram|ml|liter)s?\s*\)",
    re.IGNORECASE
)

def strip_volume_equivalent(text: str) -> str:
    return VOLUME_EQUIV_RE.sub("", text).strip()

def normalize_unit_text(text: str) -> str:
    for pattern, replacement in UNIT_MAP.items():
        text = re.sub(pattern, replacement, text, flags=re.IGNORECASE)
    return text

RANGE_RE = re.compile(
    r"^(\d+(?:\.\d+)?)\s*(?:-|\u2013|\u2014|to)\s*(\d+(?:\.\d+)?)",
    re.IGNORECASE
)

def extract_range(text: str) -> dict:
    m = RANGE_RE.match(text.strip())
    if m:
        return {
            "raw": text,
            "quantity_min": float(m.group(1)),
            "quantity_max": float(m.group(2)),
            "text": RANGE_RE.sub("", text).strip(),
        }
    return {"raw": text, "quantity_min": None, "quantity_max": None, "text": text}

PKG_RE = re.compile(
    r"^(\d+(?:\.\d+)?)\s*\(([^)]+)\)\s*(package|pkg|can|jar|bottle|box|bag|container)s?\s*(.*)",
    re.IGNORECASE
)

def _convert_fraction_in_size(text: str) -> str:
    def repl_mixed(m):
        d = float(m.group(3))
        return m.group(0) if d == 0 else str(round(float(m.group(1)) + float(m.group(2))/d, 2))
    def repl_frac(m):
        d = float(m.group(2))
        return m.group(0) if d == 0 else str(round(float(m.group(1))/d, 2))
    text = re.sub(r"(\d+)\s+(\d+)/(\d+)", repl_mixed, text)
    text = re.sub(r"\b(\d+)/(\d+)\b", repl_frac, text)
    return text

def detect_package(entry: dict) -> dict:
    m = PKG_RE.match(entry["text"])
    if m:
        entry["quantity"]     = float(m.group(1))
        entry["unit"]         = m.group(3).lower()
        entry["name"]         = m.group(4)
        entry["package_flag"] = True
        entry["package_size"] = _convert_fraction_in_size(m.group(2).strip())
    return entry

MODIFIER_WORDS = (
    r"\b(chopped|minced|diced|sliced|grated|melted|softened|beaten|"
    r"fresh|frozen|dried|crushed|ground|packed|sifted|shredded|peeled|"
    r"halved|torn|roughly|finely|thinly|coarsely|lightly|freshly|"
    r"toasted|roasted|blanched|trimmed|cubed|crumbled|divided|optional)\b"
)

def split_modifier(entry: dict) -> dict:
    text  = entry["text"]
    mods  = re.findall(MODIFIER_WORDS, text, flags=re.IGNORECASE)
    clean = re.sub(r"\s{2,}", " ", re.sub(MODIFIER_WORDS, "", text, flags=re.IGNORECASE)).strip()
    entry["modifier"] = ", ".join(mods) if mods else None
    entry["text"]     = clean
    return entry

def apply_numeric_normalization(items: list) -> list:
    items = normalize_numbers(items)
    items = [normalize_unit_text(i) for i in items]
    items = [strip_volume_equivalent(i) for i in items]
    entries = [extract_range(i) for i in items]
    entries = [detect_package(e) for e in entries]
    entries = [split_modifier(e) for e in entries]
    return entries

