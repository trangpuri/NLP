def detect_language(title: str, directions) -> dict:
    steps = []
    if isinstance(directions, str):
        try:
            parsed = ast.literal_eval(directions)
            if isinstance(parsed, list):
                steps = [str(s) for s in parsed[:3]]
        except Exception:
            steps = directions.split("\n")[:3]
    elif isinstance(directions, list):
        steps = [str(s) for s in directions[:3]]

    sample = (str(title or "") + " " + " ".join(steps)).strip()
    if len(sample) < 10:
        return {"language": "unknown", "confidence": 0.0, "is_english": False}

    try:
        results = detect_langs(sample)
        top = results[0]
        return {
            "language":   top.lang,
            "confidence": round(top.prob, 4),
            "is_english": top.lang == "en" and top.prob >= 0.90,
        }
    except LangDetectException:
        return {"language": "unknown", "confidence": 0.0, "is_english": False}


