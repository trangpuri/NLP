# --- Helper: safe field extraction from ingredient-parser-nlp ---

def safe_text(field):
    if field is None:
        return None
    if isinstance(field, list):
        parts = [i.text for i in field if hasattr(i, "text") and i.text]
        return " ".join(parts) if parts else None
    if hasattr(field, "text"):
        return field.text
    return str(field)

def is_datetime_val(val):
    return isinstance(val, (date, datetime))

def round_quantity(val):
    try:
        f = float(val)
        return int(f) if f == int(f) else round(f, 2)
    except:
        return None

LEADING_NUM_RE = re.compile(r"^(\d+(?:\.\d+)?)")

def extract_leading_qty(text: str):
    m = LEADING_NUM_RE.match(text.strip())
    return round_quantity(m.group(1)) if m else None

DECIMAL_RE   = re.compile(r"^(\d+\.\d+)\s+")
FRACTION_MAP = {0.125:"1/8",0.25:"1/4",0.333:"1/3",0.5:"1/2",0.667:"2/3",0.75:"3/4",0.875:"7/8"}

def decimal_to_fraction_str(dec_str: str) -> str:
    try:
        f = float(dec_str)
        whole = int(f)
        rem   = round(f - whole, 3)
        frac  = next((v for k, v in FRACTION_MAP.items() if abs(rem - k) < 0.02), None)
        if whole and frac: return f"{whole} {frac}"
        if whole:          return str(whole)
        if frac:           return frac
        return dec_str
    except:
        return dec_str

def rebuild_for_nlp(text: str) -> str:
    text = text.strip()
    m = DECIMAL_RE.match(text)
    if m:
        return DECIMAL_RE.sub(decimal_to_fraction_str(m.group(1)) + " ", text, count=1)
    return text

# --- Confidence Scoring ---

COUNTABLE_KEYWORDS = {
    "breast","thigh","leg","wing","drumstick","tenderloin","cutlet","fillet","filet",
    "steak","chop","rack","patty","strip","slice","shrimp","prawn","scallop","oyster",
    "mussel","clam","egg","yolk","onion","shallot","scallion","leek","clove","pepper",
    "tomato","potato","carrot","zucchini","eggplant","squash","cucumber","radish",
    "artichoke","stalk","head","floret","spear","sprig","leaf","banana","apple",
    "orange","lemon","lime","avocado","mango","peach","pear","plum","kiwi",
    "tortilla","bun","roll","pita","naan","bagel","crust","dough",
    "pepperoni","mushroom","sausage","anchovy","olive","mozzarella","parmesan","ricotta",
    "piece","portion",
}

def is_countable_ingredient(name: str) -> bool:
    name_lower = name.lower()
    return any(re.search(r"\b" + re.escape(kw) + r"\b", name_lower) for kw in COUNTABLE_KEYWORDS)

CONFIDENCE_WEIGHTS = {"quantity": 0.30, "unit": 0.25, "name": 0.30, "prep": 0.10, "no_error": 0.05}

def compute_confidence(e: dict):
    score, missing, flags = 0.0, [], []

    qty     = e.get("nlp_quantity")
    qty_min = e.get("quantity_min")
    qty_max = e.get("quantity_max")
    has_qty = (qty is not None and not is_datetime_val(qty)) or qty_min is not None or qty_max is not None

    if has_qty:
        score += CONFIDENCE_WEIGHTS["quantity"]
    else:
        missing.append("quantity"); flags.append("no_quantity")

    unit = e.get("nlp_unit")
    name = e.get("nlp_name") or ""

    if unit and str(unit).lower() not in ("none", ""):
        score += CONFIDENCE_WEIGHTS["unit"]
    elif is_countable_ingredient(name):
        score += CONFIDENCE_WEIGHTS["unit"]; flags.append("countable_no_unit")
    else:
        missing.append("unit"); flags.append("no_unit")

    if name and len(name.strip()) > 1:
        score += CONFIDENCE_WEIGHTS["name"]
    else:
        missing.append("name"); flags.append("no_name")

    if e.get("nlp_prep") or e.get("nlp_comment"):
        score += CONFIDENCE_WEIGHTS["prep"]

    if not e.get("nlp_error"):
        score += CONFIDENCE_WEIGHTS["no_error"]
    else:
        missing.append("error"); flags.append("parse_error")

    if name and len(name.strip()) == 1:
        score = max(0.0, score - 0.10); flags.append("name_too_short")
    if qty_min is not None and qty_max is not None:
        flags.append("range_quantity")

    return round(score, 2), missing, flags

def apply_confidence(e: dict) -> float:
    conf, missing, flags = compute_confidence(e)
    e["confidence"]         = conf
    e["confidence_missing"] = ", ".join(missing) if missing else None
    e["confidence_flags"]   = ", ".join(flags)   if flags   else None
    return conf

