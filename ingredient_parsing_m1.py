def has_repeated_tokens(text: str) -> bool:
    tokens = text.lower().split()
    for i in range(len(tokens) - 1):
        if tokens[i] == tokens[i + 1]:
            return True
    if len(tokens) >= 4 and len(tokens) % 2 == 0:
        half = len(tokens) // 2
        if tokens[:half] == tokens[half:]:
            return True
    return False

def is_corrupted_ingredient(text: str):
    if not isinstance(text, str) or not text.strip():
        return True, "empty_or_null"
    t = text.strip()
    if len(t) > MAX_ING_CHARS:
        return True, f"too_long:{len(t)}_chars"
    if len(t) < 2:
        return True, "too_short"
    if has_repeated_tokens(t):
        return True, f"repeated_token:{t[:40]}"
    for pattern in CORRUPTION_PATTERNS:
        if re.search(pattern, t, re.IGNORECASE):
            return True, f"pattern:{pattern[:30]}"
    return False, "ok"

def parse_ingredient_cell(raw) -> list:
    if pd.isna(raw):
        return []
    raw = str(raw).strip()
    if raw.startswith("["):
        try:
            items = ast.literal_eval(raw)
            if isinstance(items, list):
                return [str(i).strip().strip('"\'') for i in items if str(i).strip()]
        except:
            pass
    if "\n" in raw:
        return [i.strip().strip('"\'') for i in raw.split("\n") if i.strip()]
    if raw.count(",") >= 2:
        return [i.strip() for i in raw.split(",") if i.strip()]
    return [raw.strip()] if raw.strip() else []

def validate_recipe_ingredients(raw: str) -> dict:
    items = parse_ingredient_cell(raw)
    if not items:
        return {"status": "excluded", "reason": "empty_ingredient_list",
                "ingredients": [], "failed_items": []}
    if len(items) < MIN_ING_COUNT:
        return {"status": "excluded", "reason": f"too_few:{len(items)}",
                "ingredients": [], "failed_items": []}
    if len(items) > MAX_ING_COUNT:
        return {"status": "excluded", "reason": f"too_many:{len(items)}",
                "ingredients": [], "failed_items": []}
    failed = []
    for item in items:
        corrupted, reason = is_corrupted_ingredient(item)
        if corrupted:
            failed.append((item, reason))
    if failed:
        return {"status": "excluded", "reason": f"corrupted:{failed[0][1]}",
                "ingredients": [], "failed_items": failed}
    seen, clean = set(), []
    for item in items:
        k = item.strip().lower()
        if k not in seen and k:
            seen.add(k)
            clean.append(item.strip())
    return {"status": "clean", "reason": "ok", "ingredients": clean, "failed_items": []}


