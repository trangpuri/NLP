COUNTABLE_RE = re.compile(
    r"^\d+(?:\.\d+)?\s+\w+$",   # e.g. "4 eggs", "2 onions", "1 tortilla"
    re.IGNORECASE
)

def apply_pre_llm_fixes(e: dict):
    raw  = (e.get("raw") or "").strip()
    name = (e.get("nlp_name") or "").strip().lower()

    if TO_TASTE_RE.search(raw):
        e["nlp_prep"]    = ((e.get("nlp_prep") or "") + " to taste").strip()
        e["pre_llm_fix"] = "to_taste_accepted"
        return e, True

    if name in SEASONING_ONLY and e.get("nlp_quantity") is None:
        e["pre_llm_fix"] = "seasoning_no_qty_accepted"
        return e, True

    # NEW Gap B fix: also check raw string against SEASONING_ONLY
    if raw.lower() in SEASONING_ONLY and e.get("nlp_quantity") is None:
        e["pre_llm_fix"] = "seasoning_raw_accepted"
        return e, True

    # NEW Gap A fix: bare countable ingredient with no quantity — no LLM needed
    if e.get("nlp_quantity") is None and is_countable_ingredient(name):
        e["pre_llm_fix"] = "bare_countable_accepted"
        return e, True

    # Existing COUNTABLE_RE for "4 eggs" style
    if COUNTABLE_RE.match(raw) and is_countable_ingredient(
        raw.split()[1] if len(raw.split()) > 1 else raw
    ):
        if e.get("nlp_quantity") is None:
            e["nlp_quantity"] = extract_leading_qty(raw)
        e["pre_llm_fix"] = "countable_direct_accepted"
        return e, True

    if e.get("nlp_unit") and str(e["nlp_unit"]).lower() == "none":
        e["nlp_unit"]    = None
        e["pre_llm_fix"] = "unit_none_string_fixed"
        return e, False

    return e, False

# --- Layer 3: Gemini Fallback ---
# Called only when confidence < 0.85 after both Layer 1 and Layer 2 fail.
# Uses gemini-3.1-flash-lite: 500 RPD / 15 RPM — fits <5% LLM trigger target.
_last_llm_call_time = 0.0
_MIN_CALL_INTERVAL  = 4.1

def llm_parse_ingredient(raw_ingredient: str, max_retries: int = 3) -> list:
    global _last_llm_call_time

    if not raw_ingredient or not raw_ingredient.strip():
        return []
    elapsed = time.time() - _last_llm_call_time
    if elapsed < _MIN_CALL_INTERVAL:
        time.sleep(_MIN_CALL_INTERVAL - elapsed)

    system_instruction = (
        "You are an ingredient parsing API. "
        "Return ONLY valid JSON. No explanation."
    )

    prompt = (
      f"Parse the following recipe ingredient string into structured JSON.\n\n"

      f"Rules:\n"
      f"- quantity: float (fractions and mixed numbers as decimals)\n"
      f"- unit: one of [teaspoon,tablespoon,cup,ounce,pound,gram,milliliter,liter,"
      f"pint,quart,gallon,package,slice,piece,clove,bunch,sprig,pinch,dash,can,jar] or null\n"
      f"- name: core ingredient only, no prep notes\n"
      f"- modifier: prep notes (chopped, melted, divided, optional, to taste)\n"
      f"- confidence: 0.0-1.0\n\n"

      f"Important constraints:\n"
      f"- NEVER invent ingredients\n"
      f"- Only output ingredients that actually appear in the input text\n"
      f"- NEVER replace the ingredient name with another ingredient\n"
      f"- Only split into multiple objects when the text explicitly contains "
      f"'salt and pepper'\n"
      f"- Do NOT split ingredients because of parentheses, examples, or suggestions\n\n"

      f"Special cases:\n"
      f"- '2-3 cups': quantity_min=2, quantity_max=3, quantity=2\n"
      f"- 'Salt and pepper to taste': return TWO separate JSON objects in an array\n"
      f"- 'About 2 cups': quantity=2, modifier='about'\n"
      f"- Parenthetical volume notes like '(about 1 cup)' after a countable: "
      f"store in modifier, do NOT create a second object\n"
      f"- '2 medium carrots, diced (about 1 cup)' → single object, "
      f"quantity=2, name='carrots', modifier='medium, diced, about 1 cup'\n\n"

      f"Examples:\n"
      f"- 'Fresh fruit, any kind you may like (kiwi, strawberries, grapes)' → "
      f"ONE object: name='fresh fruit', modifier='any kind you may like (kiwi, strawberries, grapes)'\n"
      f"- 'Pizza toppings, of choice' → "
      f"ONE object: name='pizza toppings', modifier='of choice'\n\n"

      f"Ingredient string: {raw_ingredient}\n\n"
      f'[{{"quantity":..,"quantity_min":null,"quantity_max":null,'
      f'"unit":"..","name":"..","modifier":"..","confidence":..}}]'
    )

    payload = json.dumps({
        "system_instruction": {"parts": [{"text": system_instruction}]},
        "contents":           [{"parts": [{"text": prompt}]}],
        "generationConfig":   {"temperature": 0.0, "maxOutputTokens": 400},
    }).encode("utf-8")

    for attempt in range(max_retries):
        try:
            _last_llm_call_time = time.time()   # record just before each request
            req = urllib.request.Request(
                GEMINI_ENDPOINT,
                data=payload,
                headers={"Content-Type": "application/json"},
                method="POST"
            )
            with urllib.request.urlopen(req, timeout=45) as resp:
                data     = json.loads(resp.read().decode("utf-8"))
                raw_text = data["candidates"][0]["content"]["parts"][0]["text"].strip()

            raw_text = re.sub(r"^```json|^```|```$", "", raw_text, flags=re.MULTILINE).strip()
            parsed   = json.loads(raw_text)

            if isinstance(parsed, dict): return [parsed]
            if isinstance(parsed, list): return parsed
            return []

        except urllib.error.HTTPError as ex:
          if ex.code in (429, 503):
              wait = 5 * (attempt + 1)
              print(f"  [LLM] HTTP {ex.code} — waiting {wait}s (attempt {attempt+1}/{max_retries})")
              time.sleep(wait)
              continue

          print(f"  [LLM] HTTP {ex.code} for '{raw_ingredient}'")
          return []
        except urllib.error.URLError as ex:
            print(f"  [LLM] Network error for '{raw_ingredient}': {ex}")
            return []
        except TimeoutError:
            print(f"  [LLM] Timeout for '{raw_ingredient}' (attempt {attempt+1}/{max_retries})")
            time.sleep(5)
            continue
        except (json.JSONDecodeError, KeyError) as ex:
            print(f"  [LLM] Parse error for '{raw_ingredient}': {ex}")
            return []

    print(f"  [LLM] Max retries reached for '{raw_ingredient}'")
    return []

# --- NLP Parsing: Layer 1 + Layer 2 + Layer 3 ---

def parse_with_nlp(entries: list) -> list:
    i = 0
    while i < len(entries):
        e = entries[i]
        i += 1

        if e.get("llm_fallback"):
            continue

        # Layer 1: rule-based NLP parser
        try:
            base_text = e.get("text") or e.get("raw")
            if not base_text:
                apply_confidence(e)
                e["llm_fallback"] = False
                e["pre_llm_fix"]  = None
                continue

            nlp_input = rebuild_for_nlp(base_text)
            result    = parse_ingredient(nlp_input)

            if result.amount:
                amt = result.amount[0]
                qty = amt.quantity
                e["nlp_quantity"] = (
                    extract_leading_qty(base_text)
                    if qty is None or is_datetime_val(qty)
                    else round_quantity(qty)
                )
                e["nlp_unit"] = str(amt.unit) if amt.unit else None
            else:
                e["nlp_quantity"] = extract_leading_qty(base_text)
                e["nlp_unit"]     = None

            e["nlp_name"]    = safe_text(result.name)
            e["nlp_comment"] = safe_text(result.comment)
            e["nlp_prep"]    = safe_text(result.preparation)

        except Exception as ex:
            e["nlp_error"] = str(ex)

        e["llm_fallback"] = False
        e["pre_llm_fix"]  = None
        conf = apply_confidence(e)

        # Layer 2: pre-LLM rule fixes
        if conf < 0.85 and not e.get("nlp_error"):
            e, accepted = apply_pre_llm_fixes(e)
            if e.get("pre_llm_fix"):
                conf = apply_confidence(e)
            if accepted:
                continue

        # Layer 3: Gemini fallback — only if still below 0.85 after both layers
        if conf < 0.85 and not e.get("nlp_error") and not e.get("pre_llm_fix"):
            llm_results = llm_parse_ingredient(e.get("raw", ""))
            if llm_results:
                first = llm_results[0]
                e["nlp_quantity"] = first.get("quantity")
                e["nlp_unit"]     = first.get("unit")
                e["nlp_name"]     = first.get("name")
                e["nlp_prep"]     = first.get("modifier")
                e["llm_fallback"] = True
                apply_confidence(e)

                raw_lower = (e.get("raw") or "").lower()

                allow_split = (
                    "salt and pepper" in raw_lower
                    or "salt & pepper" in raw_lower
                )

                if allow_split:
                    for extra in llm_results[1:]:
                        extra_entry = {
                            "raw":          e.get("raw"),
                            "text":         e.get("text"),
                            "quantity_min": e.get("quantity_min"),
                            "quantity_max": e.get("quantity_max"),
                            "nlp_quantity": extra.get("quantity"),
                            "nlp_unit":     extra.get("unit"),
                            "nlp_name":     extra.get("name"),
                            "nlp_prep":     extra.get("modifier"),
                            "nlp_comment":  None,
                            "nlp_error":    None,
                            "llm_fallback": True,
                            "pre_llm_fix":  None,
                        }
                        apply_confidence(extra_entry)
                        entries.append(extra_entry)
    return entries


df2["ingredient_structured"] = df2["ingredient_structured"].apply(parse_with_nlp)
